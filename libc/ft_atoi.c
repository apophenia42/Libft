#include <libft.h>

int	ft_atoi(const char *s1)
{
	int flag;
	int i;
	int sum;

	sum = 0;
	i = 0;
	flag = 1;
	while ((*s1 >= 9 && *s1 <= 13) || (*s1 == 32))
		s1++;
	if (*s1 == '-')
	{
		flag = -1;
		s1++;
	}
	else if (*s1 == '+')
		s1++;
	while (*s1 >= '0' && *s1 <= '9')
	{
		sum = sum * 10 + (*s1 - '0');
		s1++;
	}
	return (sum * flag);
}
